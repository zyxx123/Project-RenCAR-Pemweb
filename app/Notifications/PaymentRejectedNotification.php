<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;
use App\Models\Booking;

class PaymentRejectedNotification extends Notification
{
    use Queueable;

    protected $booking;

    /**
     * Create a new notification instance.
     */
    public function __construct(Booking $booking)
    {
        $this->booking = $booking;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @return array<int, string>
     */
    public function via(object $notifiable): array
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     */
    public function toMail(object $notifiable): MailMessage
    {
        return (new MailMessage)
                    ->subject('Payment Rejected: ' . $this->booking->booking_code)
                    ->greeting('Hello ' . $notifiable->name . ',')
                    ->line('We are sorry to inform you that your payment for booking ' . $this->booking->booking_code . ' has been rejected.')
                    ->line('This might be due to an invalid proof of payment. Because of this, your booking has been cancelled and the vehicle is no longer reserved for you.')
                    ->action('Book Another Vehicle', route('home'))
                    ->line('If you think this is a mistake, please contact our support team.');
    }

    /**
     * Get the array representation of the notification.
     *
     * @return array<string, mixed>
     */
    public function toArray(object $notifiable): array
    {
        return [];
    }
}
